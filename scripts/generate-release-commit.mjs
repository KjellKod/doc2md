#!/usr/bin/env node

import { spawnSync } from "node:child_process";
import { writeFile } from "node:fs/promises";
import path from "node:path";
import process from "node:process";
import { fileURLToPath } from "node:url";

const SCRIPT_DIR = path.dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = path.resolve(SCRIPT_DIR, "..");
const OUTPUT_PATH = path.join(
  REPO_ROOT,
  "apps",
  "macos",
  "doc2md",
  "ReleaseCommit.generated.swift",
);

const UNKNOWN = "unknown";

function resolveCommit() {
  const override = process.env.DOC2MD_RELEASE_COMMIT;
  if (typeof override === "string" && override.trim().length > 0) {
    const trimmed = override.trim();
    // AC1/AC7 promise a 7-char SHA. CI shallow-clone overrides naturally land
    // as full 40-char hex (e.g. ${{ github.sha }}); slice those to keep the
    // display contract and the drift-test pattern consistent.
    if (/^[0-9a-f]{7,}$/.test(trimmed)) {
      return trimmed.slice(0, 7);
    }
    return trimmed;
  }

  const result = spawnSync("git", ["rev-parse", "--short=7", "HEAD"], {
    cwd: REPO_ROOT,
    encoding: "utf8",
  });

  if (result.status !== 0) {
    return UNKNOWN;
  }

  const stdout = (result.stdout ?? "").trim();
  if (stdout.length === 0) {
    return UNKNOWN;
  }

  return stdout;
}

function renderSwiftFile(commit) {
  return `// SPDX-License-Identifier: LicenseRef-doc2md-Desktop
// Generated by scripts/generate-release-commit.mjs. Do not edit.
enum ReleaseCommit {
    static let value: String = "${commit}"
}
`;
}

async function main() {
  const commit = resolveCommit();
  const content = renderSwiftFile(commit);
  await writeFile(OUTPUT_PATH, content, "utf8");
}

await main();
