#!/usr/bin/env node

import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const CHECK_FLAG = "--check";

function parseSupportedFormats(sourceText) {
  const match = sourceText.match(
    /export const SUPPORTED_FORMATS = \[(?<body>[\s\S]*?)\] as const;/u,
  );

  if (!match?.groups?.body) {
    throw new Error("Could not locate SUPPORTED_FORMATS in src/types/index.ts.");
  }

  const formats = Array.from(
    match.groups.body.matchAll(/"([^"]+)"/gu),
    ([, value]) => value,
  );

  if (formats.length === 0) {
    throw new Error("SUPPORTED_FORMATS is empty.");
  }

  if (!formats.includes("md")) {
    throw new Error('SUPPORTED_FORMATS must include "md".');
  }

  if (new Set(formats).size !== formats.length) {
    throw new Error("SUPPORTED_FORMATS contains duplicate entries.");
  }

  return formats;
}

function renderSwiftStringArray(values) {
  return values.map((value) => `        "${value}",`).join("\n");
}

function renderSwiftFile(supportedFormats) {
  const supportedNonMarkdownExtensions = supportedFormats.filter(
    (format) => format !== "md",
  );
  const allSupportedExtensions = [
    "md",
    "markdown",
    ...supportedNonMarkdownExtensions,
  ];

  return `import Foundation

// Generated by scripts/generate-supported-formats.mjs. Do not edit by hand.
enum SupportedFormats {
    static let supportedNonMarkdownExtensions: [String] = [
${renderSwiftStringArray(supportedNonMarkdownExtensions)}
    ]

    static let allSupportedExtensions: [String] = [
${renderSwiftStringArray(allSupportedExtensions)}
    ]
}
`;
}

async function main() {
  const scriptDir = path.dirname(fileURLToPath(import.meta.url));
  const repoRoot = path.resolve(scriptDir, "..");
  const sourcePath = path.join(repoRoot, "src/types/index.ts");
  const outputPath = path.join(
    repoRoot,
    "apps/macos/doc2md/SupportedFormats.generated.swift",
  );
  const checkMode = process.argv.includes(CHECK_FLAG);

  const sourceText = await readFile(sourcePath, "utf8");
  const supportedFormats = parseSupportedFormats(sourceText);
  const nextContent = renderSwiftFile(supportedFormats);

  let currentContent = "";
  try {
    currentContent = await readFile(outputPath, "utf8");
  } catch (error) {
    if (error?.code !== "ENOENT") {
      throw error;
    }
  }

  if (currentContent === nextContent) {
    return;
  }

  if (checkMode) {
    console.error(
      "SupportedFormats.generated.swift is out of date. Run `npm run generate:mac-supported-formats`.",
    );
    process.exitCode = 1;
    return;
  }

  await writeFile(outputPath, nextContent, "utf8");
}

await main();
